<?php
$x = 10; // Initialize x
$y = 5;  // Initialize y

$x += $y;  // Addition
echo "x += y : $x<br>";

$x -= $y;  // Subtraction
echo "x -= y : $x<br>";

$x *= $y;  // Multiplication
echo "x *= y : $x<br>";

$x /= $y;  // Division
echo "x /= y : $x<br>";

$x %= $y;  // Modulus
echo "x %= y : $x<br>";

$x **= $y; // Exponentiation
echo "x **= y : $x<br>";
?>
