<?php
$x = 10;
$y = 5;

echo "x == y: "; var_dump($x == $y); // Equal
echo "<br>";

echo "x != y: "; var_dump($x != $y); // Not equal
echo "<br>";

echo "x > y: "; var_dump($x > $y); // Greater than
echo "<br>";

echo "x < y: "; var_dump($x < $y); // Less than
echo "<br>";

echo "x >= y: "; var_dump($x >= $y); // Greater than or equal to
echo "<br>";

echo "x <= y: "; var_dump($x <= $y); // Less than or equal to
echo "<br>";
?>
