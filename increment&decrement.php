<?php
// Demonstrating Increment Operators
$x = 5; // Initial value

echo "<br>";
echo ++$x; 
// Pre-increment: $x becomes 6, then prints 6

echo "<br>";
echo $x++; 
// Post-increment: Prints 6, then $x becomes 7

echo "<br>";
echo $x; 
// Now $x is 7, so it prints 7

echo "<br><br>"; // Adding space for clarity

// Demonstrating Decrement Operators
$y = 5; // Initial value

echo --$y; 
// Pre-decrement: $y becomes 4 first, then prints 4

echo "<br>";
echo $y--; 
// Post-decrement: Prints 4, then $y becomes 3

echo "<br>";
echo $y; 
// Now $y is 3, so it prints 3
?>
